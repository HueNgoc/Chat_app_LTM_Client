    private void loadNotJoinedGroups() {
        String res = ClientSocket.getInstance()
                .sendRequest("GET_NOT_JOINED_GROUPS;" + myEmail);

        if (!res.startsWith("GROUP_LIST")) {
            JOptionPane.showMessageDialog(this, "Kh√¥ng l·∫•y ƒë∆∞·ª£c danh s√°ch group");
            return;
        }

        String[] arr = res.split(";");
        DefaultListModel<String> model = new DefaultListModel<>();

        for (int i = 1; i < arr.length; i++) {
            model.addElement(arr[i]); // id:name
        }

        listGroup.setModel(model);

    }

    private void joinSelectedGroup() {
        String selected = listGroup.getSelectedValue();
        if (selected == null) {
            JOptionPane.showMessageDialog(this, "Ch·ªçn group tr∆∞·ªõc");
            return;
        }

        String[] parts = selected.split(":");
        int groupId = Integer.parseInt(parts[0]);
        String groupName = parts[1];

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Tham gia group \"" + groupName + "\" ?",
                "Join Group",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        String res = ClientSocket.getInstance()
                .sendRequest("JOIN_GROUP;" + myEmail + ";" + groupId);

        if (res.equals("JOIN_GROUP_SUCCESS")) {
            JOptionPane.showMessageDialog(this, "Join th√†nh c√¥ng!");
            dispose(); // ƒë√≥ng dialog
        } else {
            JOptionPane.showMessageDialog(this, "Join th·∫•t b·∫°i");
        }
    }

