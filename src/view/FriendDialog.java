    private void loadUsers() {
        String res = ClientSocket.getInstance()
                .sendRequest("GET_NOT_FRIEND_USERS;" + myEmail);

        if (!res.startsWith("USER_LIST")) {
            JOptionPane.showMessageDialog(this, "Không lấy được user");
            return;
        }

        DefaultListModel<String> model = new DefaultListModel<>();
        String[] arr = res.split(";");

        for (int i = 1; i < arr.length; i++) {
            model.addElement(arr[i]); // id:email
        }

        listUser.setModel(model);
    }

