    private void btnAddFriendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddFriendActionPerformed
        // TODO add your handling code here:
        String selected = listUser.getSelectedValue();
        if (selected == null) {
            JOptionPane.showMessageDialog(this, "Chọn user trước");
            return;
        }

        String[] p = selected.split(":");
        int friendId = Integer.parseInt(p[0]);
        String email = p[1];

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Gửi lời mời kết bạn tới " + email + "?",
                "Add Friend",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        String res = ClientSocket.getInstance()
                .sendRequest("ADD_FRIEND;" + myEmail + ";" + friendId);

        if (res.equals("ADD_FRIEND_SUCCESS")) {
            JOptionPane.showMessageDialog(this, "Đã gửi lời mời!");
            loadUsers(); // refresh list
        } else {
            JOptionPane.showMessageDialog(this, "Thất bại");
        }
    }//GEN-LAST:event_btnAddFriendActionPerformed

